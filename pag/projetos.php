            <h2 class="title">English Online 3D: gamificação no ensino de língua Inglesa na modalidade a distância</h2>
                <p class="paragraph">   Na atual geração da Internet a aprendizagem de Línguas Estrangeiras/Adicionais (LE/A) pode ser
                                construída de formas diversas, tanto por meio da interação com outros, quanto por meio da produção
                                colaborativa de conteúdos online. Nesse cenário, os avanços tecnológicos, os quais facilitam o
                                compartilhamento e o gerenciamento de informações/conteúdos na Internet se tornam atividades muito mais
                                dinâmicas, pois o aluno passa a ver a Internet não apenas como uma rede de conexões para usufruir de
                                informações, mas também como um contexto de produção de conhecimentos de modo mais interativo e
                                colaborativo.</p>
                <p class="paragraph">   Diante desse panorama, o processo de elaboração de Material Didático Digital (MDD) para o ensino
                                de LE/A, especialmente para a modalidade a distância, desafia professores e educadores sobre como
                                produzir conteúdos e cursos que contemplem práticas inovadoras de ensino da linguagem, principalmente
                                explorando as particularidades do contexto digital. Tendo em vista elaborar propostas didático&#8209;pedagógicas
                                dentro de uma perspectiva de ensino de linguagem como prática social, faz-se necessário a produção de
                                MDD adequados a esses novos contextos de interação.</p>
                 
                <br>
                              
                
                
            <h2 class="title">Produção de Jogos Digitais Educacionais para o ensino de línguas na universidade</h2>
                <p class="paragraph">   Este projeto guarda-chuva investiga o desenvolvimento de jogos digitais educacionais
                                interdisciplinares e críticos em 3D para o ensino de língua inglesa. Para isso, partimos de experiência
                                prévia vivenciada durante o projeto do Jogo I-AI3 (registro GAP/CAL 033301), realizado entre os anos de
                                2012-2014, por meio do qual buscamos desenvolver o ambiente de um jogo interativo e interdisciplinar,
                                em parceria com o Labinter, explorando inicialmente mundos virtuais e outras ferramentas para promover
                                o ensino de língua inglesa e educação ambiental. No atual projeto, buscamos dar continuidade ao projeto
                                anterior de produção de (mini)jogos em inglês de modo interdisciplinar, para isso envolvemos novamente
                                diferentes públicos e participantes, tais como os alunos dos cursos de graduação em Letras, Ciência da
                                computação, Artes visuais e Desenho Industrial, para a produção dos jogos(ou minijogos).</p>
                <p class="paragraph">   A Produção de jogos digitais e/ou mundos virtuais no ensino de Línguas Estrangeiras (LE) é foco de
                                investigação por pesquisadores de diferentes áreas de conhecimento (PRENSKY, 2001; GEE, 2005;
                                MCGONIGAL, 2012; PERERA et al., 2010; VAN ECK, 2006; ULICSAK, M.; WRIGHT, 2010),
                                especialmente no cenário internacional. Por outro lado, essa prática ainda carece de mais estudos no
                                contexto brasileiro, haja vista a necessidade de entender algumas particularidades do uso desses recursos
                                nas aulas de línguas, principalmente, quando a aprendizagem de se realiza por meio de jogos digitais
                                (REIS; BILIAO, 2014; REIS; GOMES, 2015).</p>
               
                <br>
                
                
                
            <h2 class="title">Curso de Extensão ­ Formação de tutores a distância de língua Inglesa</h2>
                <p class="paragraph">   A proposta do Curso de Extensão &#8209; Formação de tutores a distância de língua Inglesa
                                (​e&#8208;Tutoring) é uma atividade de extensão a ser promovida pelo Projeto Laboratório de Ensino e
                                Aprendizagem de Línguas a Distância, em parceria com o Laboratório de Línguas da UFSM e o GrPesq
                                “Núcleo de Ensino e Aprendizagem de Línguas a Distância” (NuPEAD), sob a coordenação da
                                Professora Susana Cristina dos Reis. Com isso, preocupa&#8208;se com o desenvolvimento da fluência e
                                letramento digital dos participantes, bem como a prática de multiletramentos e habilidades linguísticas
                                necessárias para o desempenho da função em cursos a distância.</p>
                <p class="paragraph">   O e&#8208;Tutoring tem o caráter híbrido, sendo ofertado na modalidade semipresencial, mesclando
                                atividades a distância com encontros presenciais, sendo realizado nas quartas&#8209;feiras à noite,
                                explorando, por meio desta proposta, o modelo pedagógico “sala de aula invertida”, uma abordagem em
                                foco atualmente em diversas universidades no Brasil e no Exterior.</p>
